package org.example;

public class Quadruple {
    int wsize;
    double wmax;
    String listptr;
    String setptr;

    public int getWsize() {
        return wsize;
    }

    public void setWsize(int wsize) {
        this.wsize = wsize;
    }

    public double getWmax() {
        return wmax;
    }

    public void setWmax(double wmax) {
        this.wmax = wmax;
    }

    public String getListptr() {
        return listptr;
    }

    public void setListptr(String listptr) {
        this.listptr = listptr;
    }

    public String getSetptr() {
        return setptr;
    }

    public void setSetptr(String setptr) {
        this.setptr = setptr;
    }
}
